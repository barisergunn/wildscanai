  LanguageService() {
    _loadLanguage();
  }

  Future<void> _loadLanguage() async {
    final prefs = await SharedPreferences.getInstance();
    final languageCode = prefs.getString(_languageKey);
    if (languageCode != null) {
      _currentLocale = Locale(languageCode);
    }
    notifyListeners();
  }

  Future<void> setLanguage(String languageCode) async {
    final prefs = await SharedPreferences.getInstance();
    await prefs.setString(_languageKey, languageCode);
    _currentLocale = Locale(languageCode);
    notifyListeners();
  }

  Future<void> loadSavedLanguage() async {
    await _loadLanguage();
  }

  String getText(String key) {
    final languageCode = _currentLocale?.languageCode;
    if (languageCode != null && translations.containsKey(languageCode)) {
      final languageTranslations = translations[languageCode]!;
      return languageTranslations[key] ?? key;
    }
    // Fallback to English if no language is selected
    final englishTranslations = translations['en']!;
    return englishTranslations[key] ?? key;
  }

  Future<String> getCurrentLanguage() async {
    final prefs = await SharedPreferences.getInstance();
    return prefs.getString(_languageKey) ?? 'en';
  }

  Map<String, String> getSupportedLanguages() {
    return supportedLanguages.map((key, value) => MapEntry(key, value['name']!));
  }

  String getNativeName(String languageCode) {
    return supportedLanguages[languageCode]?['nativeName'] ?? languageCode;
  }

  String getFlag(String languageCode) {
    return supportedLanguages[languageCode]?['flag'] ?? 'üè≥Ô∏è';
  }

